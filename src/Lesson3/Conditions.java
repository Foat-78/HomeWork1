package Lesson3;

public class Conditions {
    public static void main(String[] args) {
        int x = 5;
        int y = 5;
        if (x > y) {
            System.out.println("x больше чем y");
        } else if (x == y){
            System.out.println("x равен y");
        } else {
            System.out.println("y больше или равен x");
        }
    }
}
